/**
 * Υπολογισμός Κινητών Εορτών για ένα δεδομένο έτος
 */
function getMovableFeasts(year: number): Record<string, string[]> {
  const easter = getOrthodoxEaster(year);
  const addDays = (date: Date, days: number): Date => {
    const res = new Date(date);
    res.setDate(res.getDate() + days);
    return res;
  };
  const toMMDD = (d: Date) =>
    `${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;

  const movable: Record<string, string[]> = {};

  // Πάσχα
  movable[toMMDD(easter)] = ['ΑΝΑΣΤΑΣΙΟΣ', 'ΑΝΑΣΤΑΣΙΑ', 'ΤΑΣΟΣ', 'ΝΑΤΑ', 'ΠΑΣΧΑΛΗΣ', 'ΠΑΣΧΑΛΙΑ'];
  // Κυριακή Βαΐων (Πάσχα - 7)
  movable[toMMDD(addDays(easter, -7))] = ['ΒΑΪΟΣ', 'ΒΑΪΑ'];
  // Λάζαρος (Πάσχα - 8)
  movable[toMMDD(addDays(easter, -8))] = ['ΛΑΖΑΡΟΣ'];
  // Ζωοδόχου Πηγής (Πάσχα + 5)
  movable[toMMDD(addDays(easter, 5))] = ['ΖΩΗ', 'ΠΗΓΗ'];
  // Θωμά (Πάσχα + 7)
  movable[toMMDD(addDays(easter, 7))] = ['ΘΩΜΑΣ', 'ΘΩΜΑΗΣ', 'ΜΑΚΗΣ'];
  // Αγ. Γεώργιος (23 Απριλίου, αλλά αν μετά το Πάσχα → Δευτέρα Πάσχα)
  const easterTime = easter.getTime();
  const stGeorgeFixed = new Date(year, 3, 23).getTime();
  if (easterTime >= stGeorgeFixed) {
    movable[toMMDD(addDays(easter, 1))] = ['ΓΕΩΡΓΙΟΣ', 'ΓΕΩΡΓΙΑ', 'ΓΙΩΡΓΟΣ', 'ΓΩΓΩ', 'ΓΙΩΡΓΗΣ'];
  } else {
    movable['04-23'] = ['ΓΕΩΡΓΙΟΣ', 'ΓΕΩΡΓΙΑ', 'ΓΙΩΡΓΟΣ', 'ΓΩΓΩ', 'ΓΙΩΡΓΗΣ'];
  }
  // Αγ. Μάρκος (25 Απριλίου, αν μετά Πάσχα → Τρίτη Πάσχα)
  const stMarkFixed = new Date(year, 3, 25).getTime();
  if (easterTime >= stMarkFixed) {
    movable[toMMDD(addDays(easter, 2))] = ['ΜΑΡΚΟΣ'];
  } else {
    movable['04-25'] = ['ΜΑΡΚΟΣ'];
  }
  // Ανάληψη (Πάσχα + 39)
  movable[toMMDD(addDays(easter, 39))] = ['ΑΝΑΛΗΨΗ'];
  // Πεντηκοστή (Πάσχα + 49) - Πνευματική εορτή
  movable[toMMDD(addDays(easter, 49))] = ['ΠΝΕΥΜΑΤΙΑΣ'];
  // Αγ. Πνεύμα (Πάσχα + 50)
  movable[toMMDD(addDays(easter, 50))] = ['ΠΝΕΥΜΑΤΙΟΣ'];

  return movable;
}

/**
 * Επιστρέφει λίστα κανονικοποιημένων ονομάτων που εορτάζουν σε δεδομένη ημέρα
 */
export function getCelebratingNamesForDate(date: Date): string[] {
  const mmdd = `${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  const year = date.getFullYear();

  const movables = getMovableFeasts(year);
  const namesFixed = FIXED_FEASTS[mmdd] || [];
  const namesMovable = movables[mmdd] || [];

  // Κανονικοποίηση
  const all = [...namesFixed, ...namesMovable].map(n => normalizeGreekName(n));
  return [...new Set(all)]; // deduplicate
}

/**
 * Ελέγχει αν ένα όνομα εορτάζει σε δεδομένη ημέρα.
 * Χρησιμοποιεί παρωνύμια, prefix matching.
 */
export function isNameday(name: string, date: Date): boolean {
  const celebratingList = getCelebratingNamesForDate(date);
  if (celebratingList.length === 0) return false;

  const normName = normalizeGreekName(name);
  // Resolve nickname → canonical
  const resolved = NICKNAME_MAP[normName] ?? normName;

  return celebratingList.some(celeb => {
    const normCeleb = normalizeGreekName(celeb);
    if (normName === normCeleb || resolved === normCeleb) return true;
    // Prefix match (για παραλλαγές π.χ. ΔΗΜΗΤΡΗΣ ↔ ΔΗΜΗΤΡΙΟΣ)
    const minLen = Math.min(normName.length, normCeleb.length, 5);
    if (minLen >= 4 && normName.substring(0, minLen) === normCeleb.substring(0, minLen)) return true;
    return false;
  });
}
